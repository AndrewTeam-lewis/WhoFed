<h2 class="text-xl font-bold mb-2">Privacy Policy for WhoFed</h2>
<p class="text-gray-500 text-xs mb-6">Effective Date: January 30, 2026</p>

<p>At WhoFed, we know that your privacy—and your pet’s routine—is important. This Privacy Policy explains how we collect, use, and protect your information when you use our mobile application.</p>

<h3>1. Information We Collect</h3>
<p>We only collect the minimum information necessary to make sure your pet gets fed:</p>
<ul>
    <li><strong>Account Information:</strong> Your first name, last name, username, email address, and password.</li>
    <li><strong>Pet Information:</strong> Your pet’s name.</li>
    <li><strong>Optional Contact Information:</strong> Your phone number, if you choose to opt-in for SMS feeding reminders.</li>
    <li><strong>Technical Data:</strong> Basic device information (OS version) and app usage logs to help us fix bugs and crashes.</li>
</ul>

<h3>2. How We Use Your Information</h3>
<p>We use your data strictly to:</p>
<ul>
    <li>Create and secure your account.</li>
    <li>Send you reminders (via push notifications or SMS).</li>
    <li>Allow other members of your household to see who fed the pet.</li>
    <li>Provide customer support when you email us.</li>
</ul>

<h3>3. Data Storage and Security</h3>
<ul>
    <li><strong>Security:</strong> We use industry-standard encryption to protect your password and personal data.</li>
    <li><strong>Location:</strong> Your data is stored on secure servers located in the United States.</li>
    <li><strong>Retention:</strong> We keep your data only as long as your account is active. If you delete your account, your personal data is purged from our active databases.</li>
</ul>

<h3>4. Sharing Your Information</h3>
<p>We do not sell, rent, or trade your personal information to third parties. We only share data with service providers who help us run the app (for example, an email service to send password resets or an SMS provider for notifications). These providers are legally bound to keep your data private.</p>

<h3>5. Your Rights & Choices (GDPR & CCPA)</h3>
<p>Regardless of your location, we provide the following "Privacy for All" rights:</p>
<ul>
    <li><strong>Access & Portability:</strong> You can request a copy of the data we have on you.</li>
    <li><strong>Correction:</strong> You can update your account details anytime inside the app.</li>
    <li><strong>Deletion:</strong> You have the "Right to be Forgotten." You may delete your account at any time.</li>
    <li><strong>Opt-Out:</strong> You can opt-out of SMS notifications at any time through your device settings or by removing your phone number from your profile.</li>
</ul>

<h3>6. Children’s Privacy</h3>
<p>WhoFed is not intended for children under the age of 13. We do not knowingly collect personal information from children. If we learn we have collected such info, we will delete it immediately.</p>

<h3>7. Michigan Privacy Rights</h3>
<p>As a Michigan-based entity, we comply with the Michigan Identity Theft Protection Act. We maintain reasonable procedures to protect your data and will notify you in the event of a data breach as required by law.</p>

<h3>8. Changes to This Policy</h3>
<p>We may update this policy occasionally. If we make significant changes, we will notify you via the email address associated with your account.</p>

