<h2 class="text-xl font-bold mb-2">Privacy Policy for WhoFed</h2>
<p class="text-gray-500 text-xs mb-6">Effective Date: May 3, 2026</p>

<p>At WhoFed, we know that your privacy—and your pet's routine—is important. This Privacy Policy explains how we collect, use, and protect your information when you use our mobile application.</p>

<h3>1. Information We Collect</h3>
<p>We only collect information necessary to provide the WhoFed service:</p>
<ul>
    <li><strong>Account Information:</strong> Your first name, email address, and password (or OAuth identity via Google or Apple).</li>
    <li><strong>Pet Information:</strong> Your pet's name, species, icon, timezone, and care schedules.</li>
    <li><strong>Pet Photos:</strong> Images you upload as pet avatars, stored in our cloud storage.</li>
    <li><strong>Activity Logs:</strong> Records of when care tasks (feeding, medication, litter) are completed and by whom.</li>
    <li><strong>Household Data:</strong> Which users belong to which households and their permission levels.</li>
    <li><strong>Subscription Status:</strong> Whether your account is on a free or premium tier.</li>
    <li><strong>Push Notification Token:</strong> A device token used to deliver push notifications, collected only if you grant notification permission.</li>
    <li><strong>Language Preference:</strong> Stored locally on your device to remember your preferred language.</li>
</ul>

<h3>2. How We Use Your Information</h3>
<p>We use your data strictly to:</p>
<ul>
    <li>Create and secure your account.</li>
    <li>Synchronize pet care tasks across household members.</li>
    <li>Send push notification reminders (only if you opt in).</li>
    <li>Manage your subscription and unlock premium features.</li>
    <li>Provide customer support when you contact us.</li>
</ul>

<h3>3. Data Storage and Security</h3>
<ul>
    <li><strong>Encryption in Transit:</strong> All data sent between the app and our servers is encrypted using HTTPS/TLS.</li>
    <li><strong>Encryption at Rest:</strong> Data is stored on secure Supabase-managed servers with industry-standard encryption.</li>
    <li><strong>Location:</strong> Your data is stored on servers located in the United States.</li>
    <li><strong>Access Control:</strong> Row-level security policies ensure users can only access data belonging to their own household.</li>
    <li><strong>Retention:</strong> We keep your data only as long as your account is active. If you delete your account, your personal data is purged from our systems.</li>
</ul>

<h3>4. Third-Party Services We Use</h3>
<p>We use the following third-party services to operate WhoFed. Each receives limited data necessary for their function and is bound by their own privacy policy:</p>
<ul>
    <li><strong>Supabase</strong> – Provides our backend database, authentication, and file storage. All app data (user profiles, pets, activity logs, photos) is stored on Supabase infrastructure. <a href="https://supabase.com/privacy" class="text-brand-sage hover:underline">supabase.com/privacy</a></li>
    <li><strong>RevenueCat</strong> – Manages in-app subscriptions on Android. RevenueCat receives your user ID, subscription entitlements, and purchase history to verify premium access. <a href="https://www.revenuecat.com/privacy" class="text-brand-sage hover:underline">revenuecat.com/privacy</a></li>
    <li><strong>Stripe</strong> – Processes web-based payments. Stripe receives payment method and billing information when you subscribe via the web. <a href="https://stripe.com/privacy" class="text-brand-sage hover:underline">stripe.com/privacy</a></li>
    <li><strong>Google Firebase</strong> – Delivers push notifications on Android via Firebase Cloud Messaging (FCM). Firebase receives your device notification token. <a href="https://policies.google.com/privacy" class="text-brand-sage hover:underline">policies.google.com/privacy</a></li>
    <li><strong>Google OAuth</strong> – Used for optional "Sign in with Google." We receive your email and name from Google upon sign-in. <a href="https://policies.google.com/privacy" class="text-brand-sage hover:underline">policies.google.com/privacy</a></li>
    <li><strong>Apple Sign In</strong> – Used for optional "Sign in with Apple." We receive your email and name from Apple upon sign-in. <a href="https://www.apple.com/privacy/" class="text-brand-sage hover:underline">apple.com/privacy</a></li>
</ul>
<p>We do not sell, rent, or trade your personal information to any third parties beyond those listed above.</p>

<h3>5. Your Rights & Choices (GDPR & CCPA)</h3>
<p>Regardless of your location, we provide the following rights:</p>
<ul>
    <li><strong>Access & Portability:</strong> You can view all your data within the app and request a copy at any time.</li>
    <li><strong>Correction:</strong> You can update your account details anytime inside the app.</li>
    <li><strong>Deletion:</strong> You may delete your account at any time within the app's Settings menu, or by requesting deletion through our web portal at <a href="https://whofed.me/delete-account" class="text-brand-sage hover:underline">whofed.me/delete-account</a>. Deleting your account removes all associated data from our systems.</li>
    <li><strong>Notifications:</strong> You can disable push notifications at any time in your device settings.</li>
</ul>

<h3>6. Children's Privacy</h3>
<p>WhoFed is not intended for children under the age of 13. We do not knowingly collect personal information from children. If we learn we have collected such info, we will delete it immediately.</p>

<h3>7. Michigan Privacy Rights</h3>
<p>As a Michigan-based entity, we comply with the Michigan Identity Theft Protection Act. We maintain reasonable procedures to protect your data and will notify you in the event of a data breach as required by law.</p>

<h3>8. Changes to This Policy</h3>
<p>We may update this policy occasionally. If we make significant changes, we will notify you via the email address associated with your account.</p>

