<h2 class="text-xl font-bold mb-2">Política de Privacidade (Privacy Policy)</h2>
<p class="text-gray-500 text-xs mb-6">Data de Vigência: 30 de janeiro de 2026</p>

<p>No WhoFed, sabemos que sua privacidade — e a rotina do seu pet — é importante. Esta Política de Privacidade explica como coletamos, usamos e protegemos suas informações ao usar nosso aplicativo móvel.</p>

<h3>1. Informações que Coletamos</h3>
<p>Coletamos apenas o mínimo de informações necessárias para garantir que seu pet seja alimentado:</p>
<ul>
    <li><strong>Informações da Conta:</strong> Seu nome, sobrenome, nome de usuário, endereço de e-mail e senha.</li>
    <li><strong>Informações do Pet:</strong> O nome do seu animal de estimação.</li>
    <li><strong>Informações de Contato Opcionais:</strong> Seu número de telefone, caso você opte por receber lembretes de alimentação via SMS.</li>
    <li><strong>Dados Técnicos:</strong> Informações básicas do dispositivo (versão do SO) e logs de uso do aplicativo para nos ajudar a corrigir erros e falhas.</li>
</ul>

<h3>2. Como Usamos Suas Informações</h3>
<p>Usamos seus dados estritamente para:</p>
<ul>
    <li>Criar e proteger sua conta.</li>
    <li>Enviar lembretes (via notificações push ou SMS).</li>
    <li>Permitir que outros membros da sua casa vejam quem alimentou o pet.</li>
    <li>Fornecer suporte ao cliente quando você nos envia um e-mail.</li>
</ul>

<h3>3. Armazenamento e Segurança dos Dados</h3>
<ul>
    <li><strong>Segurança:</strong> Usamos criptografia padrão da indústria para proteger sua senha e dados pessoais.</li>
    <li><strong>Localização:</strong> Seus dados são armazenados em servidores seguros localizados nos Estados Unidos.</li>
    <li><strong>Retenção:</strong> Mantemos seus dados apenas enquanto sua conta estiver ativa. Se você excluir sua conta, seus dados pessoais serão removidos de nossos bancos de dados ativos.</li>
</ul>

<h3>4. Compartilhamento de Suas Informações</h3>
<p>Não vendemos, alugamos ou comercializamos suas informações pessoais com terceiros. Apenas compartilhamos dados com provedores de serviços que nos ajudam a operar o aplicativo (por exemplo, um serviço de e-mail para redefinir senhas ou um provedor de SMS). Esses provedores são contratualmente obrigados a manter seus dados privados.</p>

<h3>5. Seus Direitos e Escolhas (LGPD, GDPR e CCPA)</h3>
<p>Independentemente de sua localização, oferecemos os seguintes direitos de "Privacidade para Todos":</p>
<ul>
    <li><strong>Acesso e Portabilidade:</strong> Você pode solicitar uma cópia dos dados que temos sobre você.</li>
    <li><strong>Correção:</strong> Você pode atualizar os detalhes da sua conta a qualquer momento dentro do aplicativo.</li>
    <li><strong>Exclusão:</strong> Você tem o "Direito ao Esquecimento". Você pode excluir sua conta a qualquer momento.</li>
    <li><strong>Revogação de Consentimento:</strong> Você pode cancelar as notificações de SMS a qualquer momento através das configurações do seu dispositivo ou removendo seu número de telefone do seu perfil.</li>
</ul>

<h3>6. Privacidade das Crianças</h3>
<p>O WhoFed não é destinado a crianças menores de 13 anos. Não coletamos intencionalmente informações pessoais de crianças. Se soubermos que coletamos tais informações, as excluiremos imediatamente.</p>

<h3>7. Direitos de Privacidade Regionais (Brasil e EUA)</h3>
<ul>
    <li><strong>Brasil (LGPD):</strong> Em conformidade com a Lei Geral de Proteção de Dados (Lei nº 13.709/2018), o WhoFed atua como Controlador de Dados. Você tem o direito de confirmar a existência do tratamento de dados e solicitar a anonimização ou bloqueio de dados desnecessários.</li>
    <li><strong>Michigan (EUA):</strong> Como uma entidade sediada em Michigan, cumprimos a Lei de Proteção contra Roubo de Identidade de Michigan, notificando você em caso de violação de dados, conforme exigido por lei.</li>
</ul>

<h3>8. Alterações nesta Política</h3>
<p>Podemos atualizar esta política ocasionalmente. Se fizermos alterações significativas, notificaremos você através do e-mail associado à sua conta.</p>

