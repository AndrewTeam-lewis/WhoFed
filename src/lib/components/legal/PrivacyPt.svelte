<h2 class="text-xl font-bold mb-2">Política de Privacidade (Privacy Policy)</h2>
<p class="text-gray-500 text-xs mb-6">Data de Vigência: 3 de maio de 2026</p>

<p>No WhoFed, sabemos que sua privacidade — e a rotina do seu pet — é importante. Esta Política de Privacidade explica como coletamos, usamos e protegemos suas informações ao usar nosso aplicativo móvel.</p>

<h3>1. Informações que Coletamos</h3>
<p>Coletamos apenas as informações necessárias para fornecer o serviço WhoFed:</p>
<ul>
    <li><strong>Informações da Conta:</strong> Seu nome, endereço de e-mail e senha (ou identidade OAuth via Google ou Apple).</li>
    <li><strong>Informações do Pet:</strong> Nome, espécie, ícone, fuso horário e rotinas de cuidado do seu animal.</li>
    <li><strong>Fotos do Pet:</strong> Imagens que você envia como avatar do pet, armazenadas em nossa nuvem.</li>
    <li><strong>Registros de Atividade:</strong> Registros de quando as tarefas de cuidado (alimentação, medicação, caixa de areia) são concluídas e por quem.</li>
    <li><strong>Dados do Lar:</strong> Quais usuários pertencem a quais lares e seus níveis de permissão.</li>
    <li><strong>Status da Assinatura:</strong> Se sua conta está no plano gratuito ou premium.</li>
    <li><strong>Token de Notificação Push:</strong> Um token de dispositivo usado para enviar notificações push, coletado apenas se você conceder permissão.</li>
    <li><strong>Preferência de Idioma:</strong> Armazenada localmente no seu dispositivo para lembrar seu idioma preferido.</li>
</ul>

<h3>2. Como Usamos Suas Informações</h3>
<p>Usamos seus dados estritamente para:</p>
<ul>
    <li>Criar e proteger sua conta.</li>
    <li>Sincronizar tarefas de cuidado do pet entre os membros do lar.</li>
    <li>Enviar lembretes via notificações push (somente se você optar por isso).</li>
    <li>Gerenciar sua assinatura e desbloquear recursos premium.</li>
    <li>Fornecer suporte ao cliente quando você nos contatar.</li>
</ul>

<h3>3. Armazenamento e Segurança dos Dados</h3>
<ul>
    <li><strong>Criptografia em Trânsito:</strong> Todos os dados enviados entre o aplicativo e nossos servidores são criptografados usando HTTPS/TLS.</li>
    <li><strong>Criptografia em Repouso:</strong> Os dados são armazenados em servidores gerenciados pelo Supabase com criptografia padrão da indústria.</li>
    <li><strong>Localização:</strong> Seus dados são armazenados em servidores localizados nos Estados Unidos.</li>
    <li><strong>Controle de Acesso:</strong> Políticas de segurança em nível de linha garantem que os usuários só possam acessar dados pertencentes ao seu próprio lar.</li>
    <li><strong>Retenção:</strong> Mantemos seus dados apenas enquanto sua conta estiver ativa. Se você excluir sua conta, seus dados pessoais serão removidos de nossos sistemas.</li>
</ul>

<h3>4. Serviços de Terceiros que Utilizamos</h3>
<p>Utilizamos os seguintes serviços de terceiros para operar o WhoFed. Cada um recebe dados limitados necessários para sua função e está sujeito à sua própria política de privacidade:</p>
<ul>
    <li><strong>Supabase</strong> – Fornece nosso banco de dados backend, autenticação e armazenamento de arquivos. Todos os dados do aplicativo (perfis de usuário, pets, registros de atividade, fotos) são armazenados na infraestrutura do Supabase. <a href="https://supabase.com/privacy" class="text-brand-sage hover:underline">supabase.com/privacy</a></li>
    <li><strong>RevenueCat</strong> – Gerencia assinaturas dentro do aplicativo no Android. O RevenueCat recebe seu ID de usuário, direitos de assinatura e histórico de compras para verificar o acesso premium. <a href="https://www.revenuecat.com/privacy" class="text-brand-sage hover:underline">revenuecat.com/privacy</a></li>
    <li><strong>Stripe</strong> – Processa pagamentos via web. O Stripe recebe informações de pagamento e cobrança quando você assina pelo site. <a href="https://stripe.com/privacy" class="text-brand-sage hover:underline">stripe.com/privacy</a></li>
    <li><strong>Google Firebase</strong> – Entrega notificações push no Android via Firebase Cloud Messaging (FCM). O Firebase recebe o token de notificação do seu dispositivo. <a href="https://policies.google.com/privacy" class="text-brand-sage hover:underline">policies.google.com/privacy</a></li>
    <li><strong>Google OAuth</strong> – Usado para o login opcional "Entrar com o Google". Recebemos seu e-mail e nome do Google ao fazer login. <a href="https://policies.google.com/privacy" class="text-brand-sage hover:underline">policies.google.com/privacy</a></li>
    <li><strong>Apple Sign In</strong> – Usado para o login opcional "Entrar com a Apple". Recebemos seu e-mail e nome da Apple ao fazer login. <a href="https://www.apple.com/privacy/" class="text-brand-sage hover:underline">apple.com/privacy</a></li>
</ul>
<p>Não vendemos, alugamos ou comercializamos suas informações pessoais com terceiros além dos listados acima.</p>

<h3>5. Seus Direitos e Escolhas (LGPD, GDPR e CCPA)</h3>
<p>Independentemente de sua localização, oferecemos os seguintes direitos:</p>
<ul>
    <li><strong>Acesso e Portabilidade:</strong> Você pode visualizar todos os seus dados dentro do aplicativo e solicitar uma cópia a qualquer momento.</li>
    <li><strong>Correção:</strong> Você pode atualizar os detalhes da sua conta a qualquer momento dentro do aplicativo.</li>
    <li><strong>Exclusão:</strong> Você pode excluir sua conta a qualquer momento no menu Configurações do aplicativo, ou solicitando a exclusão através do nosso portal em <a href="https://whofed.me/delete-account" class="text-brand-sage hover:underline">whofed.me/delete-account</a>. A exclusão da conta remove todos os dados associados de nossos sistemas.</li>
    <li><strong>Notificações:</strong> Você pode desativar notificações push a qualquer momento nas configurações do dispositivo.</li>
</ul>

<h3>6. Privacidade das Crianças</h3>
<p>O WhoFed não é destinado a crianças menores de 13 anos. Não coletamos intencionalmente informações pessoais de crianças. Se soubermos que coletamos tais informações, as excluiremos imediatamente.</p>

<h3>7. Direitos de Privacidade Regionais (Brasil e EUA)</h3>
<ul>
    <li><strong>Brasil (LGPD):</strong> Em conformidade com a Lei Geral de Proteção de Dados (Lei nº 13.709/2018), o WhoFed atua como Controlador de Dados. Você tem o direito de confirmar a existência do tratamento de dados e solicitar a anonimização ou bloqueio de dados desnecessários.</li>
    <li><strong>Michigan (EUA):</strong> Como uma entidade sediada em Michigan, cumprimos a Lei de Proteção contra Roubo de Identidade de Michigan, notificando você em caso de violação de dados, conforme exigido por lei.</li>
</ul>

